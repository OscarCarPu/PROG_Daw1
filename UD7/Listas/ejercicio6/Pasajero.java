package ejercicio6;

import java.util.*;

public class Pasajero{
  private String dni;
  private String nombre;
  private String apellido;
  private int edad;
  private String nacionalidad;

  public Pasajero(String dni,String nombre,String apellido,int edad,String nacionalidad){
    this.dni=dni;
    this.nombre=nombre;
    this.apellido=apellido;
    this.edad=edad;
    this.nacionalidad=nacionalidad;
  }  
}